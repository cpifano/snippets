//Función anónima autoejecutable:
(() => {
  console.log("Inicio");

  function dos(){
    console.log("Dos");
  }

  function uno(){
    console.log("Uno");
    dos();
  }

  uno();
  console.log("Fin");
})();
//---------------------------------------------------------------------------------------------------------------------------//


//---------------------------------------------------------------------------------------------------------------------------//
// ASÍNCRONO [NO BLOQUEANTE]:
//---------------------------------------------------------------------------------------------------------------------------//
(() => {
  console.log("Inicio");

  function dos(){
    setTimeout(() => {
      console.log("Dos");
    }, 1000);
  }

  function uno(){
    setTimeout(() => {
      console.log("Dos");
    }, 0);

    dos();
  }

  uno();
  console.log("Fin");
})();
