// CREATE OBJECT ID FUNCTION:
//---------------------------------------------------------------------------------------------------------------------------//
function getObjectId() {
  const timestamp = (new Date().getTime() / 1000 | 0).toString(16);
  const suffix = 'xxxxxxxxxxxxxxxx'.replace(/[x]/g, () => (Math.random() * 16 | 0).toString(16)).toLowerCase()
  return `${timestamp}${suffix}`;
}
//---------------------------------------------------------------------------------------------------------------------------//


//---------------------------------------------------------------------------------------------------------------------------//
// OBJECT ID REGEX TO VALIDATE AN OBJECT ID:
//---------------------------------------------------------------------------------------------------------------------------//
const regexObjectId = /^[0-9a-fA-F]{24}$/;
const newObjectId = getObjectId();

if(regexObjectId.test(newObjectId)){
  console.log('Es un ObjectId válido!');
} else {
  console.log('NO es un ObjectId válido.');
}
